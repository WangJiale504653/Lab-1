import time
RED="\x1b[48;5;1m"
WHITE="\x1b[48;5;15m"
BLUE="\x1b[48;5;27m"
RESET="\x1b[0m"
flag_length=3
flag_width=50
for i in range(1,flag_length+1):
   print(f"{RED}{" "*flag_width}{RESET}")
   time.sleep(0.5)
for i in range(1,flag_length+1):
   print(f"{WHITE}{" "*flag_width}{RESET}")
   time.sleep(0.5)
for i in range(1,flag_length+1):
   print(f"{BLUE}{" "*flag_width}{RESET}")
   time.sleep(0.5)
